static inline int check_result(void *result)
{
	int i = 0;

	printf("source date:\n");
	while (i < 250) {
		if (i % 10 == 0 && i != 0)
			printf("\n");
		printf("%2x ", zlib_test[i]);
		i++;
	}
	printf("\n");

	printf("dst date:\n");
	i = 0;
	while (i < 250) {
		if (i % 10 == 0 && i != 0)
			printf("\n");
		printf("%2x ", ((char *)result)[i]);
		i++;
	}
	printf("\n");

	if (memcmp(result, right_result, 1024)) {
		printf("test fail\n");
		return -1;
	}

	return 0;
}

int main(int argc, char *argv[])
{
	struct wd_capa capa;
	struct wd_queue q;
	struct wd_zip_msg *msg;
	void *a, *src, *dst, *sq;
	int ret;
	int output_num;
	int fd;
	FILE *fp;
	/* add your pasid here */
	unsigned long pasid = 0;
	
	memset(&q, 0, sizeof(q));
	memset(&capa, 0, sizeof(capa));
	capa.alg = "zlib";
	capa.throughput = 10;
	capa.latency = 10;

	ret = wd_request_queue(&q, &capa);
	SYS_ERR_COND(ret, "wd_request_queue");
	ret = wd_set_pasid(&q);
	SYS_ERR_COND(ret, "wd_set_pasid");

	/* Allocate some space and setup a DMA mapping */
	a = mmap(0, ASIZE,
		 PROT_READ | PROT_WRITE, MAP_PRIVATE | MAP_ANONYMOUS, 0, 0);
	memset(a, 0, ASIZE);
	ret = wd_mem_share(&q, a, ASIZE, 0);
	SYS_ERR_COND(ret, "wd_mem_share err\n");
	printf("WD dma map VA=IOVA=%p successfully!\n", a);


	src = a;
	dst = (char *)a + 2048;
	memcpy(src, zlib_test, 244);

	msg = (struct wd_zip_msg *)malloc(sizeof(struct wd_zip_msg));
	msg->src = (__u64)src;
	msg->dst = (__u64)dst;
	msg->size = 244;
	/* now we only support pbuffer */
	msg->aflags = 0;

	ret = wd_send(&q, msg); // fix me: input zlib info in second parameter.
	SYS_ERR_COND(ret, "send fail(release queue should be done auto)\n");
	
	ret = wd_recv_sync(&q, 0, 0); //fix me
	SYS_ERR_COND(ret, "recv fail(release queue should be done auto)\n");
	printf("here\n");
	sleep(10);

	/* add zlib compress head and write head + compressed date to a file */
	sq = ((struct hzip_queue_info *)q.priv)->sq_base;
	output_num = *((__u32 *)sq + 1);
	printf("output_num: %d\n", output_num);
	char zip_head[2] = {0x78, 0x9c};

	fp = fopen("/root/compress_date", "wb");

	fwrite(zip_head, 1, 2, fp);
	fwrite(dst, 1, output_num, fp);

	fclose(fp);

	ret = check_result(dst);
	if (ret)
		printf("zlib fail!\n");

	free(msg);
	wd_mem_unshare(&q, a, ASIZE);
	munmap(a, ASIZE);

	ret = wd_unset_pasid(&q);
	SYS_ERR_COND(ret, "wd_unset_pasid");

	wd_release_queue(&q);

	return EXIT_SUCCESS;
}
