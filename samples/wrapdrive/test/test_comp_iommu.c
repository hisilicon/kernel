static inline int check_result(void *result)
{
	int i = 0;

	printf("source date:\n");
	while (i < 250) {
		if (i % 10 == 0 && i != 0)
			printf("\n");
		printf("%2x ", zlib_test[i]);
		i++;
	}
	printf("\n");

	printf("dst date:\n");
	i = 0;
	while (i < 250) {
		if (i % 10 == 0 && i != 0)
			printf("\n");
		printf("%2x ", ((char *)result)[i]);
		i++;
	}
	printf("\n");

	if (memcmp(result, right_result, 1024)) {
		printf("test fail\n");
		return -1;
	}

	return 0;
}
#define COMP_FILE	"/root/compress_data"
int main(int argc, char *argv[])
{
	struct wd_capa capa;
	struct wd_queue q;
	struct wd_comp_msg *msg;
	void *a, *src, *dst, *sq;
	int ret, i;
	int output_num;
	FILE *fp;
	int proc_tag;
	char file[64];

	if (argv[1])
		proc_tag = strtoul(argv[1], NULL, 10);
	else
		proc_tag = 0;
	if (proc_tag)
		sprintf(file, COMP_FILE"%d", proc_tag);
	else
		sprintf(file, COMP_FILE);
	memset(&q, 0, sizeof(q));
	memset(&capa, 0, sizeof(capa));
	capa.alg = zlib;
	capa.throughput = 10;
	capa.latency = 10;

	ret = wd_request_queue(&q, &capa);
	SYS_ERR_COND(ret, "wd_request_queue");
	printf("\npasid=%d, dma_flag=%d",q.pasid, q.dma_flag);

	/* Allocate some space and setup a DMA mapping */
	a = mmap((void *)0xffffa9001000, ASIZE,
		 PROT_READ | PROT_WRITE, MAP_PRIVATE | MAP_ANONYMOUS | MAP_FIXED, 0, 0);
	if (a != (void *)0xffffa9001000) {
		printf("\nmmap fail!");
		goto release_q;
	}
	memset(a, 0, ASIZE);
	sleep(5);
	ret = wd_mem_share(&q, a, ASIZE, 0);
	SYS_ERR_COND(ret, "wd_mem_share err\n");
	printf("WD dma map VA=IOVA=%p successfully!\n", a);

	src = a;
	dst = (char *)a + (ASIZE/2);

	for (i = 0; i < 128; i++)
		memcpy(src + i * 256, zlib_test, 244);

	msg = malloc(sizeof(*msg));
	if (!msg) {
		printf("\nalloc msg fail!");
		goto alloc_msg_fail;
	}
	memset((void *)msg, 0, sizeof(*msg));
	for (i = 0; i < 1024; i++) {
		msg->alg = zlib;
		msg->src = (__u64)src + ( i % 128) * 256;
		msg->dst = (__u64)dst + ( i % 128) * 256;
		msg->in_bytes = 244;

		/* now we only support pbuffer */
		msg->aflags |= _WD_AATTR_IOVA;
		ret = wd_send(&q, msg);
		SYS_ERR_COND(ret, "send fail(release queue should be done auto)\n");
		sleep(1);
		ret = wd_recv_sync(&q, 0, 0);
		SYS_ERR_COND(ret, "recv fail(release queue should be done auto)\n");

		/* add zlib compress head and write head + compressed date to a file */
		sq = ((struct hzip_queue_info *)q.priv)->sq_base;
		output_num = *((__u32 *)sq + 32 * i + 1);
		printf("output_num: %d\n", output_num);
	}
	char zip_head[2] = {0x78, 0x9c};

	fp = fopen(file, "wb");

	fwrite(zip_head, 1, 2, fp);
	fwrite((char *)msg->dst, 1, output_num, fp);

	fclose(fp);
#if 0
	ret = check_result(dst);
	if (ret)
		printf("zlib fail!\n");
#endif
	free(msg);
alloc_msg_fail:
	wd_mem_unshare(&q, a, ASIZE);
	munmap(a, ASIZE);
release_q:
	wd_release_queue(&q);

	return EXIT_SUCCESS;
}
